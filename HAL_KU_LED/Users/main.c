#include "./SYSTEM/sys/sys.h"
#include "led.h"
#include "./SYSTEM/delay/delay.h"
int main()
{
     HAL_Init();                                 /* 初始化HAL库 */
    sys_stm32_clock_init(RCC_PLL_MUL9);         /* 设置时钟,72M */
    delay_init(72);                             /* 初始化延时函数 */
    led_init();                                 /* 初始化LED */
while(1)
{
    HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_5);
    delay_ms(500);
}
    
}
    
    

